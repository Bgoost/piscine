char	*ft_strlowcase(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 'A' && *str <= 'Z')
		{
			*str = *str + 32;
			str++;
			i++;
		}
		else
		{
			str++;
			i++;
		}
	}
	if (*str == '\0')
	{
		str = str - i;
	}
	return (str);
}
