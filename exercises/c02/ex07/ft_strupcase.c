char	*ft_strupcase(char *str)
{
	int	i;

	i = 0;
	while (*str != '\0')
	{
		if (*str >= 'a' && *str <= 'z')
		{
			*str = *str - 32;
			str++;
			i++;
		}
		else
		{
			str++;
			i++;
		}
	}
	if (*str == '\0')
	{
		str = str - i;
	}
	return (str);
}
